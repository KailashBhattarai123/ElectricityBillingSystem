    public BillRecords(Object cid,String username) {
        initComponents();
        this.customerid=cid;
        this.username=username;
        dtm=(DefaultTableModel)jTable1.getModel();
        dtm.setRowCount(0);
        Statement st=DBConnection.statementObject();
       String sql="SELECT * FROM biiling_info WHERE cid="+cid+" ORDER BY `biiling_info`.`final_reading` DESC";
        try {
             ResultSet rs=st.executeQuery(sql);
        
          while(rs.next()){
              int units=rs.getInt("final_reading")-rs.getInt("pre_reading");
              int price=units*11;
              
              dtm.addRow(new Object[]{rs.getInt("biid"),rs.getInt("cid"),rs.getDate("date"),rs.getInt("pre_reading"),rs.getInt("final_reading"),units,price,rs.getString("status"),rs.getString("paidto")});
          }
            System.out.println("data displayed");
            
            
        } catch (Exception e) {
            System.out.println("data not displayed"+e);
        }
    }

